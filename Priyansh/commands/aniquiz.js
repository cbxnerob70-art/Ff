module.exports.handleReaction = ({ api, event, handleReaction }) => {
    if (event.userID !== handleReaction.author) return;
    let response = "";
    if (event.reaction === "👍") response = "True";
    else if (event.reaction === "😢") response = "False";
    else return;

    if (response === handleReaction.answer) {
        api.sendMessage("🎉 Congrats, you got the answer right!", event.threadID);
    } else {
        api.sendMessage("❌ Oops, wrong answer!", event.threadID);
    }

    const index = global.client.handleReaction.findIndex(
        e => e.messageID === handleReaction.messageID
    );
    global.client.handleReaction.splice(index, 1);
    handleReaction.answerYet = 1;
    return global.client.handleReaction.push(handleReaction);
};

module.exports.run = async ({ api, event }) => {
    const randomQuestion = animeQuestions[Math.floor(Math.random() * animeQuestions.length)];

    return api.sendMessage(
        `🎌 Anime Quiz Time!\n\n🧠 Question:\n${randomQuestion.question}\n\n👍: True     😢: False`,
        event.threadID,
        async (err, info) => {
            global.client.handleReaction.push({
                name: "aniquiz",
                messageID: info.messageID,
                author: event.senderID,
                answer: randomQuestion.correct_answer,
                answerYet: 0
            });

            await new Promise(resolve => setTimeout(resolve, 20 * 1000));

            const index = global.client.handleReaction.findIndex(
                e => e.messageID === info.messageID
            );
            if (index !== -1 && global.client.handleReaction[index].answerYet !== 1) {
                api.sendMessage(
                    `⏰ Time's up!\nCorrect answer was: ${randomQuestion.correct_answer}`,
                    event.threadID
                );
                return global.client.handleReaction.splice(index, 1);
            }
        }
    );
};
